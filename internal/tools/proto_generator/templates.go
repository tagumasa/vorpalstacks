// Package proto_generator provides utilities for generating Protocol Buffer definitions
// from Smithy models. This package handles type mapping and code generation.
package proto_generator

import (
	"fmt"
	"regexp"
	"strings"
	"text/template"
)

var baseProtoTemplate = `// Code generated by proto_generator. DO NOT EDIT.
// source: {{.PackageName}}

syntax = "proto3";

package {{.PackageName}};

option go_package = "{{.GoPackage}}";
{{if .DartPackage}}
option dart_package = "{{.DartPackage}}";
{{end}}
import "common.proto";
import "aws.proto";

// {{.ServiceName}} provides {{.PackageName}} API operations.
service {{.ServiceName}} {
{{range .Operations}}
  // {{.Comment | sanitizeComment}}
  // HTTP: {{.HTTPMethod}} {{.HTTPURIPath}}{{if .HTTPURIQuery}}?{{.HTTPURIQuery}}{{end}}
  // Protocol: {{.Protocol}}
  rpc {{.Name}}({{.InputShape | emptyToCommonEmpty}}) returns ({{.OutputShape | emptyToCommonEmpty}});
{{end}}
}

{{range .Shapes}}
{{if .Comment}}
// {{.Comment | sanitizeComment}}
{{end}}{{if .IsEnum}}
enum {{.Name}} {
{{range .EnumValues}}
  {{.Name}} = {{.Index}};
{{end}}
}
{{else}}
message {{.Name}} {
{{range .Fields}}
  {{.Type}} {{.NameLower}} = {{.Number}}{{if .HTTPPayload}} [(aws.http_payload) = true]{{end}};
{{end}}
}
{{end}}
{{end}}
`

var htmlTagRegex = regexp.MustCompile(`<[^>]*>`)

func sanitizeComment(s string) string {
	s = htmlTagRegex.ReplaceAllString(s, "")
	s = strings.ReplaceAll(s, "\n", " ")
	s = strings.ReplaceAll(s, "\r", " ")
	s = strings.ReplaceAll(s, "//", "/")
	s = strings.ReplaceAll(s, "/*", "/")
	s = strings.ReplaceAll(s, "*/", "/")
	s = regexp.MustCompile(`\s+`).ReplaceAllString(s, " ")
	s = strings.TrimSpace(s)
	if len(s) > 200 {
		s = s[:197] + "..."
	}
	return s
}

func templateFuncs() template.FuncMap {
	return template.FuncMap{
		"sanitizeComment":    sanitizeComment,
		"emptyToCommonEmpty": emptyToCommonEmpty,
		"httpRule":           httpRule,
	}
}

func httpRule(method, path, query string) string {
	if method == "" || path == "" {
		return ""
	}

	rule := fmt.Sprintf("method: \"%s\", uri: \"%s\"", method, path)
	if query != "" {
		rule += fmt.Sprintf(", query: \"%s\"", query)
	}
	return rule
}

func emptyToCommonEmpty(s string) string {
	if s == "" {
		return "common.Empty"
	}
	return s
}

// GetInternalGrpcTemplate returns the internal gRPC template
func GetInternalGrpcTemplate() *template.Template {
	return template.Must(template.New("internalGrpc").Funcs(templateFuncs()).Parse(baseProtoTemplate))
}

// GetFlutterGrpcTemplate returns the Flutter gRPC template
func GetFlutterGrpcTemplate() *template.Template {
	return template.Must(template.New("flutterGrpc").Funcs(templateFuncs()).Parse(baseProtoTemplate))
}

var internalGrpcTemplate = GetInternalGrpcTemplate()
var flutterGrpcTemplate = GetFlutterGrpcTemplate()
